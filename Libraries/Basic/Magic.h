#pragma once

#if __LITTLE_ENDIAN__
#define K_STRING_U64_2(s) 0 \
    | ((u64)s[7]) << (7 * 8) \
    | ((u64)s[6]) << (6 * 8) \
    | ((u64)s[5]) << (5 * 8) \
    | ((u64)s[4]) << (4 * 8) \
    | ((u64)s[3]) << (3 * 8) \
    | ((u64)s[2]) << (2 * 8) \
    | ((u64)s[1]) << (1 * 8) \
    | ((u64)s[0]) << (0 * 8)

#define K_CSTRING_U64_2(s) 0   \
    | ((u64)'\0') << (7 * 8)    \
    | ((u64)s[6]) << (6 * 8)    \
    | ((u64)s[5]) << (5 * 8)    \
    | ((u64)s[4]) << (4 * 8)    \
    | ((u64)s[3]) << (3 * 8)    \
    | ((u64)s[2]) << (2 * 8)    \
    | ((u64)s[1]) << (1 * 8)    \
    | ((u64)s[0]) << (0 * 8)

#elif __BIG_ENDIAN__

#define K_STRING_U64_2(s) 0 \
    | ((u64)s[0]) << (7 * 8) \
    | ((u64)s[1]) << (6 * 8) \
    | ((u64)s[2]) << (5 * 8) \
    | ((u64)s[3]) << (4 * 8) \
    | ((u64)s[4]) << (3 * 8) \
    | ((u64)s[5]) << (2 * 8) \
    | ((u64)s[6]) << (1 * 8) \
    | ((u64)s[7]) << (0 * 8)

#define K_CSTRING_U64_2(s) 0   \
    | ((u64)s[0]) << (7 * 8)    \
    | ((u64)s[1]) << (6 * 8)    \
    | ((u64)s[2]) << (5 * 8)    \
    | ((u64)s[3]) << (4 * 8)    \
    | ((u64)s[4]) << (3 * 8)    \
    | ((u64)s[5]) << (2 * 8)    \
    | ((u64)s[6]) << (1 * 8)    \
    | ((u64)'\0') << (0 * 8)
#else
#error "unknown endian"
#endif
#define K_STRING_U64(s) K_STRING_U64_2(s "\0\0\0\0\0\0\0\0")
#define K_STRING_U64(s) K_STRING_U64_2(s "\0\0\0\0\0\0\0\0")

#define K_CSTRING_U64(s) K_CSTRING_U64_2(s "\0\0\0\0\0\0\0\0")
#define K_CSTRING_U64(s) K_CSTRING_U64_2(s "\0\0\0\0\0\0\0\0")
